                <p style="text-align: justify">É notório que a Defensoria Pública da União tem envidado enormes esforços no sentido de interiorizar sua atuação, tendo esta agenda ganhado impulso com o advento da Emenda Constitucional
                    80/2014, que fixa prazo de 08 (oito) anos para que a União, os Estados e o Distrito Federal contem com Defensores Públicos em todas as unidades jurisdicionais.
                    Enquanto a total interiorização não é alcançada, a Defensoria Pública da União se utiliza das chamadas “ações itinerantes” para atender a demanda dos assistidos residentes em cidades
                    vinculadas a subseções judiciárias ainda não guarnecidas com unidade instalada do órgão. Atualmente, as missões itinerantes são implementadas por meio de dois projetos: o “Eu Tenho Direito”
                    e o “Defensoria Para Todos”. O Projeto Eu Tenho Direito foi lançado em agosto de 2014, através do Processo SEI nº 08038.003525/2014-95, com objetivo de "propiciar o acesso à justiça por meio
                    de medidas judiciais ou extrajudiciais visando garantir os direitos aos assistidos que residem em localidades onde não há unidades da Defensoria Pública da União", sendo que o planejamento é
                    centralizado na Secretaria de Atuação Itinerante (SIT), vinculada à Secretaria-Geral de Articulação Institucional (SGAI). No início por meio do referido projeto, deslocou-se Defensores Públicos
                    Federais e servidores para diversas cidades, de todas as regiões do país, com especial enfoque nos municípios mais carentes, com IDH (índice de desenvolvimento humano) mais baixo.
                </p>
