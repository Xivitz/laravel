            <div class="col-md-offset-8">
                <p style="text-align: justify">
                    Entretanto, com o desenrolar do projeto, constatou-se que o acompanhamento remoto, pela SIT/SGAI, das ações judiciais propostas, na prática, mostrou-se inviável, quer seja em razão da necessidade de realização de
                    atos presenciais (audiências, por exemplo), quer seja pela necessidade de contato com o assistido para obtenção de novos documentos e/ou cumprimento de diligências judiciais, o que obstaculizou seu
                    seguimento nos termos inicialmente pensados.
                    Em verdade, notou-se que esse modelo hierarquizado de atuação itinerante, planejado e definido a partir de critérios eleitos pela Administração Superior, não atingia o fim ao qual se destinava, porquanto
                    o acompanhamento das ações judiciais, feito de forma centralizada em Brasília, encontrou barreiras operacionais e orçamentárias deveras intransponíveis. O programa sobrevive hoje com foco em ações feitas
                    em conjunto com outras instituições. A título de exemplo, cita-se a parceria com o Ministério do Trabalho, mediante a qual Defensores Públicos Federais são deslocados para integrarem as missões de resgate
                    de trabalhadores em condições análogas à escravidão, em locais remotos do país. Os processos de assistência jurídica (PAJs) instaurados em decorrência destes resgates são acompanhados remotamente pelo Grupo
                    de Trabalho de Erradicação ao Trabalho Escravo (GT Trabalho Escravo), vinculado à SGAI.
                    De sua vez, o Projeto Defensoria para Todos foi lançado em março de 2016, por meio do Processo SEI n.º 08038.001817/2016-55 (TAP n. 1194193). Já atento às dificuldades do modelo desenhado no seio do Projeto
                    “Eu Tenho Direito”, esta nova iniciativa funda-se na descentralização do planejamento e da execução de ações itinerantes voltadas ao público alvo da Defensoria Pública da União. Agora, estas atribuições passaram
                    a ser afetas às próprias Unidades de atuação da Defensoria Pública da União já instaladas, com o objetivo de "propiciar o acesso à justiça, difusão de informações e conscientização de direitos a pessoas em situação
                    de vulnerabilidade e que residem em localidade distante ou de difícil acesso às unidades da DPU". Atividades como divulgação, contato com as autoridades e a sociedade civil da cidade a ser atendida, preparação
                    da infraestrutura para o atendimento, dentre outras, passaram a ser de responsabilidade da Unidade, por meio da Coordenação local do projeto. A atuação da SGAI, neste cenário, resume-se à verificação da adequação do
                    TAP enviado aos termos, objetivos e diretrizes do Projeto, de forma a garantir uniformidade mínima na execução, bem como o controle dos recursos financeiros, dividindo-os de forma equânime entre todos os Defensores.
                    Em seu primeiro ano de implementação efetiva, o Projeto “Defensoria Para Todos” teve adesão de praticamente metade das unidades instaladas da DPU e levou os serviços de assistência jurídica gratuita a mais de 80
                    (oitenta) cidades do interior do país, ocasionando a abertura de mais de 4.000 (quatro mil PAJs) e a realização de milhares de atendimentos. Alguns prefeitos, inclusive, enviaram expediente exaltando a atuação da
                    Defensoria e solicitando o regresso da ação itinerante no ano de 2017.
                    Conquanto passível de ajustes e aprimoramentos, o projeto “Defensoria Para Todos” vem se mostrado exitoso em seu mister de prover assistência jurídica gratuita aos assistidos que ainda não contam com a Defensoria
                    Pública da União instalada em sua cidade, além de representar inegável ganho institucional, pois promove a divulgação e expansão do serviço defensorial, tornando a instituição mais próxima dos usuários.
                </p>
